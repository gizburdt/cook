# Environment

A project goes through several stages. It starts with development, during which existing migrations are often adjusted. Once the project has been deployed to a production (or staging) environment, this is no longer possible. Follow the instructions below for the different project stages.

STATUS: development

---

## When status == development
- Adjust existing migrations when possible; do not create new ones

## When status == production
- Do not modify existing translations without permission when asked to translate the application
