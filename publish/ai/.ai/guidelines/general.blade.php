# General

## Languages
- Use American spelling, like "color" instead of "colour"
- Use English in `__()` and `@lang()` methods and translate the value

## Dates
- Use `UTC` in the database
- Use `d-m-Y H:i` format for datetime
- Use `d-m-Y` format for date
- Use `H:i` format for time

## Prices
- When working with prices, store them as cents
- Don't use `private` properties and methods, rather use `protected`
- Don't use the `compact` function in PHP
